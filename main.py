from server import mcp

# =============================================================================
# MCPサーバーの起動
# =============================================================================
# mcp.run() のトランスポート方式:
#
# | 方式            | 用途                         | 通信方法              |
# |-----------------|------------------------------|-----------------------|
# | stdio           | ローカル環境                 | 標準入出力（stdin/out）|
# | streamable-http | リモート公開（HTTP経由）     | HTTP + ストリーミング |
#
# -------------------------------------------------------------------------
# stdio（Standard Input/Output）
# -------------------------------------------------------------------------
# - Claude Desktop などローカルのMCPクライアントとの通信に使用
# - プロセス間通信（IPC）で直接やりとり
# - 設定が簡単、ローカル開発向け
# - Claude Desktop の設定例:
#   {
#     "mcpServers": {
#       "terminal-server": {
#         "command": "python",
#         "args": ["main.py"]
#       }
#     }
#   }
#
# -------------------------------------------------------------------------
# streamable-http（HTTP + ストリーミング）
# -------------------------------------------------------------------------
# - リモートサーバーとしてHTTP経由で公開する場合に使用
# - SSE（Server-Sent Events）の後継で、双方向通信が可能
# - 複数クライアントからのアクセスに対応
# - 使用例:
#   mcp.run("streamable-http", host="0.0.0.0", port=8000)
#
# -------------------------------------------------------------------------
# 使い分けの基準
# -------------------------------------------------------------------------
# - ローカルで自分だけ使う → stdio
# - ネットワーク越しに公開する → streamable-http
# - Claude Desktop (command + args 形式) → stdio
# =============================================================================

if __name__ == "__main__":
  mcp.run("stdio")
